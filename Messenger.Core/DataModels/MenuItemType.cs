namespace Messenger.Core
{
    /// <summary>
    /// The types of items for a menu item
    /// </summary>
    public enum MenuItemType
    {
        /// <summary>
        /// Shows the menu text and an icon
        /// </summary>
        TextAndIcon = 0,

        /// <summary>
        /// Shows a simple divider beetween the menu items
        /// </summary>
        Divider = 1,

        /// <summary>
        ///  Shows the menu text as header
        /// </summary>
        Header = 2
    }
}
